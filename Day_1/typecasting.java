package Day_1;

public class typecasting {
    public static void main(String[] args) {
        int a = 10;
        float b = (float) (a);
        System.out.println(b);

        String s = "Hello vamshi";
        char c = s.charAt(3);
        String g = "" + c;
        System.out.println(c);
        System.out.println(g);

    }
}
